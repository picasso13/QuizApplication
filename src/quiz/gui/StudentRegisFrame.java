    private void jbRegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbRegActionPerformed
        // TODO add your handling code here:
        int res=validateInput();
          System.out.println("in Reg");
        
        if(res==-1){            
           JOptionPane.showMessageDialog(null,"Passwords do not match", "Invalid!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(res==-2){            
            JOptionPane.showMessageDialog(null,"Userid or password length less than 4", "Invalid inputs!!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(res==0){            
            JOptionPane.showMessageDialog(null,"Please fill all the fields", "Empty Values!!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(res==1){
        
            try {
               
                Quiz q=new Quiz();
                q.setUserType("student");
                q.setUserId(uid);
                q.setUserPass(upass);
                if(QuizDAO.addUser(q)){
                JOptionPane.showMessageDialog(null,"Student added", "Success!", JOptionPane.INFORMATION_MESSAGE);
                clearAll();
            }
                else{
                   JOptionPane.showMessageDialog(null,"Userid already exists", "Error!", JOptionPane.ERROR_MESSAGE); 
                }
            } catch (SQLException ex) {
                Logger.getLogger(StudentRegisFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jbRegActionPerformed

