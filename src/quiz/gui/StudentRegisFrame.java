private int validateInput() {
    System.out.println("in function");
       uid=txtUserName.getText();
       upass=new String(txtUserPass.getPassword());
       reupass=new String(txtReUserPass.getPassword());
       if(uid.isEmpty() || upass.isEmpty()||reupass.isEmpty())
           return 0;
       if(uid.length()<4||upass.length()<4){
          System.out.println("inside"); return -2;}
       if(!upass.equals(reupass))
              return -1;
            
       return 1;
      
    }
