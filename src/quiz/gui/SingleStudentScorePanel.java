private void loadStudentId(){
    try{
    ArrayList <String> usid=PerformanceDAO.getAllStudent();
    if(usid.isEmpty()){
        JOptionPane.showMessageDialog(null, "No student has given test","Empty",JOptionPane.ERROR_MESSAGE);  
        return;
    }
    jcStudentId.removeAllItems();
    for(String stu:usid)
            jcStudentId.addItem(stu);
    
}       catch (SQLException ex) {
            Logger.getLogger(SingleStudentScorePanel.class.getName()).log(Level.SEVERE, null, ex);
        }}

