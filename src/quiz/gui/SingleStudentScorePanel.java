    private void jcStudentIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcStudentIdActionPerformed
        // TODO add your handling code here:
       // System.out.println("Inside stuId");
        if(studentIdNotSelected){
            studentIdNotSelected=false;
            return;
        }
      try{
          examIdNotSelected=true;
          ArrayList <String> exids=PerformanceDAO.getAllExamId(jcStudentId.getSelectedItem().toString());
          jcExId.removeAllItems();
          for(String ex:exids){
             // System.out.println(ex+ " ");
              jcExId.addItem(ex);
            
          }
         // studentIdNotSelected=true; 
      } catch (SQLException ex) {
          Logger.getLogger(SingleStudentScorePanel.class.getName()).log(Level.SEVERE, null, ex);
      }
    }//GEN-LAST:event_jcStudentIdActionPerformed

    private void jcExIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcExIdActionPerformed
      
         if(examIdNotSelected){
              examIdNotSelected=false;
              return;
          }        
        try {
            
          // TODO add your handling code here:
        // System.out.println("In exId");
          String stuid=jcStudentId.getSelectedItem().toString();
         // System.out.println("stuid="+stuid);
          String exid=jcExId.getSelectedItem().toString();
           //System.out.println("exid="+exid);
          if(exid.equals("choose...")){
              JOptionPane.showMessageDialog(null, "Please choose","Empty",JOptionPane.ERROR_MESSAGE);
              return;}          
          StudentScore score=PerformanceDAO.getScore(stuid,exid);
          txtLang.setText(score.getLanguage());
          txtPer.setText(String.valueOf(score.getPer()));
      } catch (SQLException ex) {
          Logger.getLogger(SingleStudentScorePanel.class.getName()).log(Level.SEVERE, null, ex);
      }
        
    }//GEN-LAST:event_jcExIdActionPerformed

